def get_stroke_count(name, name_type="auto"):
    """
    Lấy số nét của một chữ trong tên
    
    Args:
        name: Tên cần tra cứu (sẽ được chuyển thành CHỮ IN HOA)
        name_type: "ho" (họ), "dem" (tên đệm), "ten" (tên), "auto" (tự động tìm)
    
    Returns:
        Số nét nếu tìm thấy, None nếu không có
    """
    # Chuẩn hóa: chuyển thành chữ in hoa, bỏ dấu cách thừa
    name_upper = name.upper().strip()
    
    if name_type == "ho":
        return HO_STROKE_COUNT.get(name_upper)
    elif name_type == "dem":
        return TEN_DEM_STROKE_COUNT.get(name_upper)
    elif name_type == "ten":
        return TEN_STROKE_COUNT.get(name_upper)
    else:
        # Tự động tìm trong tất cả các bảng
        if name_upper in HO_STROKE_COUNT:
            return HO_STROKE_COUNT[name_upper]
        if name_upper in TEN_DEM_STROKE_COUNT:
            return TEN_DEM_STROKE_COUNT[name_upper]
        if name_upper in TEN_STROKE_COUNT:
            return TEN_STROKE_COUNT[name_upper]
        return None


def get_all_stroke_data():
    """Trả về tất cả dữ liệu số nét để tham khảo"""
    return {
        "ho": HO_STROKE_COUNT,
        "ten_dem": TEN_DEM_STROKE_COUNT,
        "ten": TEN_STROKE_COUNT
    }
